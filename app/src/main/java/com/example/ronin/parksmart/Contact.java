public class Contact {

    String contactno, name, username, email, pass, plate;

    public void setContactno(String cn) {this.contactno = cn;}
    public String getContactno() {return this.contactno;}

    public void setName(String nname) {this.name = nname;}
    public String getName() {return this.name;}

    public void setUsername(String nusername) {this.username = nusername;}
    public String getUsername() {return this.username;}

    public void setEmail(String nemail) {this.email = nemail;}
    public String getEmail() {return this.email;}

    public void setPass(String npass) {this.pass = npass;}
    public String getPass() {return this.pass;}

    public void setPlate(String nplate) {this.plate = nplate;}
    public String getPlate() {return this.plate;}

}
