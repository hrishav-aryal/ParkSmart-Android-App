    public void onclick(View v){
        EditText ed= (EditText) findViewById(R.id.search);
        String location = ed.getText().toString();
        List<Address> adlist = null;
        if(location!= null || !location.equals("")){
            Geocoder geocoder= new Geocoder(this);
            try {
                adlist = geocoder.getFromLocationName(location,1);

            } catch (IOException e) {
                e.printStackTrace();
            }

            Address address = adlist.get(0);
            LatLng latlng = new LatLng(address.getLatitude(),address.getLongitude());
            mMap.addMarker(new MarkerOptions().position(latlng).title(location));
            mMap.animateCamera(CameraUpdateFactory.newLatLngZoom(new LatLng(address.getLatitude(), address.getLongitude()), 12.0f));

        }
    }

